package hello_world;

/** Primo programma in Java:
 *  Uso delle variabili e dei tipi primitivi.
 */
public class HelloWorld {

	public static void main(String[] args) {
		
	    // Variabili numeriche
		int numeroIntero = 10;
		double numeroDecimale = 3.14;
		
		// Variabile testuale
		String messaggio = "Ciao nel mondo Java";
		
		// Variabile booleana
		boolean condizione = true;
		
		// Variabile carattere singolo
		char iniziale = 'L';
		
	     // Stampa a video
        System.out.println("Hello World!");
        System.out.println("Numero intero: " + numeroIntero);
        System.out.println("Numero decimale: " + numeroDecimale);
        System.out.println("Messaggio: " + messaggio);
        System.out.println("Condizione: " + condizione);
        System.out.println("Iniziale: " + iniziale);


	}

}
